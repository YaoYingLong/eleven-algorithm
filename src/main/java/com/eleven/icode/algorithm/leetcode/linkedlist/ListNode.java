package com.eleven.icode.algorithm.leetcode.linkedlist;

/**
 * @author by Eleven on 2022/3/4
 */
public class ListNode {
    public int val;
    public ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
